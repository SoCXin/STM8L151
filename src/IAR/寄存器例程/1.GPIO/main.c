#include"iostm8l151k4.h"
/*******************************************************************************
****入口参数：无
****出口参数：无
****函数备注：GPIO测试程序，PA6灌流方式连接LED，通过控制PA6输出电平，使LED闪烁
****版权信息：蓝旗嵌入式系统
*******************************************************************************/
void main( void )
{
    int i,j;
    PA_DDR_DDR6=1;           //端口数据方向寄存器，输出
    PA_CR1_C16=1;            //端口控制寄存器1，推挽输出
    PA_CR2_C26=0;            //端口控制寄存器2，输出速率2MHz
    while (1)
    {
      PA_ODR_ODR6=1;  //端口输出数据寄存器，输出高电平
      for(i=0;i<100;i++)
        for(j=0;j<200;j++);
      PA_ODR_ODR6=0;  //端口输出数据寄存器，输出低电平
      for(i=0;i<100;i++)
        for(j=0;j<200;j++);
           
     
     }

}
